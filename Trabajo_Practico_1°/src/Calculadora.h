#include <stdio.h>

/**
 * @fn float PedirNumero(char*)
 * @brief Pide un numero float
 *
 * @param mensaje Muestra un mensaje de lo que quieras pedir
 * @return Retorna el numero pedido
 */
float PedirNumero(char* mensaje);

/**
 * @fn float Suma(float, float)
 * @brief Suma dos numero flotantes
 *
 * @param x Suma x con y
 * @param y
 * @return Retorna el resultado de la suma
 */
float Suma(float x, float y);

/**
 * @fn float Resta(float, float)
 * @brief Resta dos numero flotantes
 *
 * @param x	Resta x - y
 * @param y
 * @return Retorna el resultado de la suma
 */
float Resta(float x, float y);

/**
 * @fn float Division(float, float)
 * @brief Divide dos numero flotantes
 *
 * @param x Divide x / y
 * @param y
 * @return retorna el resultado de la division
 */
float Division(float x, float y);

/**
 * @fn float Multiplicacion(float, float)
 * @brief Multiplica dos numero flotantes
 *
 * @param x Multiplica x * y
 * @param y
 * @return Retorna el resultado de la multiplicacion
 */
float Multiplicacion(float x, float y);

/**
 * @fn float Factorial(float)
 * @brief Busca el facotrial de un numero
 *
 * @param numero Busca el factorial de numero
 * @return Retorna el factorial encontrado
 */
float Factorial(float numero);
